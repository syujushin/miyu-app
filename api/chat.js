export default async function handler(req, res) {
  if (req.method !== 'POST') return res.status(405).json({ error: 'Method Not Allowed' })
  try {
    const { messages } = req.body

    // 마지막 사용자 메시지로 서버사이드 태그 매칭
    const lastUserMsg = [...messages].reverse().find(m => m.role === 'user')?.content ?? ''
    const matched = findMatchedProducts(lastUserMsg)

    // 매칭된 제품이 있으면 시스템 프롬프트에 강제 주입
    let finalPrompt = SYSTEM_PROMPT
    if (matched.length > 0) {
      finalPrompt += `\n\n[서버 사전 검색 결과 — 반드시 recommended_products에 포함]\n`
      matched.forEach(p => {
        finalPrompt += `• ${p.name} / ${p.price}원\n`
      })
      finalPrompt += `\n위 제품은 목록에 실제로 존재합니다. "없다", "찾을 수 없다"는 표현은 절대 사용하지 마세요.`
    }

    const client     = new OpenAI({ apiKey: process.env.OPENAI_API_KEY })
    const completion = await client.chat.completions.create({
      model: 'gpt-4o-mini',
      messages: [{ role: 'system', content: finalPrompt }, ...messages],
      max_tokens: 1024,
      response_format: { type: 'json_object' },
    })
    res.status(200).json({ response: completion.choices[0].message.content })
  } catch (err) {
    res.status(500).json({ error: err.message })
  }
}
