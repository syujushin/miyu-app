function findMatchedProducts(userMessage) {
  if (!userMessage) return []
  // 공백 제거 + 소문자로 정규화해서 띄어쓰기 차이를 무시
  const msg = userMessage.toLowerCase().replace(/\s+/g, '')

  const scored = PRODUCTS.map(p => {
    const score = p.tags.reduce((acc, tag) => {
      return acc + (msg.includes(tag.toLowerCase().replace(/\s+/g, '')) ? 1 : 0)
    }, 0)
    return { ...p, score }
  }).filter(p => p.score > 0).sort((a, b) => b.score - a.score)

  return scored.slice(0, 3)
}

